    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        int aviso = JOptionPane.showConfirmDialog(rootPane, "Estas seguro de eliminar");
        if (aviso == 0) {
            DetalleCuentasBancariasBD oDetalleCuentasBancariasBD = new DetalleCuentasBancariasBD();
            boolean rpta = oDetalleCuentasBancariasBD.eliminarDetalleCuentasBancarias(idcuenta);
            if (rpta) {
                exito("Todo OK");
                limpiar();
                limpiar_tabla_formulario();
            } else {
                error("Tienes problemas para eliminar");
            }
            
        }
    }//GEN-LAST:event_btnEliminarActionPerformed


